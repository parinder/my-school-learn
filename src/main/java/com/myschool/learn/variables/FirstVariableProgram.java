package com.myschool.learn.variables;

public class FirstVariableProgram {


    public static void main(String[] args) {
        int integerTypeMainMethodVariable = 9;
        String stringTypeMainMethodVariable = "String Type Variable in Main() Method";
        int myFirstVariable = 10 + 5;

        System.out.println("The Value of Integer type Variable is: " + integerTypeMainMethodVariable);
        System.out.println(stringTypeMainMethodVariable);
        System.out.println("The Value of myFirstVariable Integer type Variable is: " + myFirstVariable);

    }

}
